public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1274f;
        System.out.printf("Formatted number is %.2f",a);
        System.out.println();
        System.out.printf("%.3f\n",Math.PI);
        System.out.printf("My name is %s and I am %s","aastik","cool");
    }
}
